#!/usr/bin/env python3
"""Install-parity validator for hand-maintained sibling copies.

Many artifacts in this repo live in multiple trees that the build pipeline does
NOT auto-generate end-to-end. The shared-agent family is the canonical
example: ``templates/agents/X.shared.md`` is the source of truth that the
``generate_agents`` script propagates to ``src/claude/``,
``src/copilot-cli/agents/``, and ``src/vs-code-agents/``. The Claude-Code
self-host copy ``.claude/agents/X.md`` and the GitHub-Copilot self-host copy
``.github/agents/X.agent.md`` are hand-maintained and are NOT touched by the
generator (REQ-003-010 forbids generators from writing under ``.claude/``).

When an author updates the template but forgets to refresh the install copies,
``main`` ends up in a state where the published vendored copies and the
in-repo self-host copies disagree on what the agent prompt is. PR #2087
(qa.shared.md) and PR #2083 (orchestrator.shared.md) are concrete examples:
both updated ``templates/`` and ``src/`` but skipped ``.claude/agents/``,
leaving the in-repo Claude Code self-host stale.

This validator catches that drift at PR time. It does NOT regenerate. It
reports the sibling files that should have changed together and did not.

PARITY GROUPS (sibling rules)
-----------------------------

Every parity group is a set of files that MUST move together. If any one of
them is in the diff, every other member of the group that exists on disk
(or that should exist for a shared agent name) must also be in the diff.

1. SHARED_AGENT
   Anchor: ``templates/agents/{name}.shared.md``
   Members:
     - ``templates/agents/{name}.shared.md``
     - ``.claude/agents/{name}.md``
     - ``.github/agents/{name}.agent.md``
     - ``src/claude/{name}.md``
     - ``src/copilot-cli/agents/{name}.agent.md``
     - ``src/vs-code-agents/{name}.agent.md``
   Notes: a freestanding ``.github/agents/X.agent.md`` (no template) is not
   a SHARED_AGENT member; it forms its own one-member group.

2. RULE
   Anchor: ``.claude/rules/{name}.md`` (or its install mirrors)
   Members:
     - ``.claude/rules/{name}.md``
     - ``.github/instructions/{name}.instructions.md``
     - ``src/copilot-cli/instructions/{name}.instructions.md``

Skills, commands, hooks, and lib are auto-generated by ``build/scripts/build_all.py``
and their drift is caught by ``build_all.py --check``. They are intentionally
out of scope for this validator.

CLI
---

::

    python3 build/scripts/validate_install_parity.py                  # diff vs @{push}..HEAD
    python3 build/scripts/validate_install_parity.py --base origin/main
    python3 build/scripts/validate_install_parity.py --files a b c    # explicit set, for tests
    python3 build/scripts/validate_install_parity.py --format json

EXIT CODES (per ADR-035)
------------------------

0 - all parity groups consistent (or nothing relevant changed)
1 - drift detected (one or more groups missing siblings)
2 - configuration error (repo root not found, git unavailable when needed)
"""

from __future__ import annotations

import argparse
import json
import re
import subprocess
import sys
from collections.abc import Iterable, Sequence
from dataclasses import dataclass, field
from pathlib import Path

# --- Repo layout ---------------------------------------------------------

_REPO_ROOT = Path(__file__).resolve().parent.parent.parent

# Names excluded from agent groups; they are directory metadata, not agents.
# Stored as both the bare filename and the bare stem so callers can match
# either ``Path.name`` ("AGENTS.md") or the captured regex group ("AGENTS").
_AGENT_FILENAME_BLOCKLIST: frozenset[str] = frozenset(
    {"AGENTS.md", "CLAUDE.md", "AGENTS", "CLAUDE"}
)

# Names excluded from rule groups; same rationale.
_RULE_FILENAME_BLOCKLIST: frozenset[str] = frozenset(
    {"CLAUDE.md", "AGENTS.md", "AGENTS", "CLAUDE"}
)


# --- Parity group model --------------------------------------------------


@dataclass(frozen=True)
class ParityGroup:
    """One co-change requirement: every member must move together."""

    kind: str
    name: str
    members: tuple[str, ...]


@dataclass
class Violation:
    """One sibling missing from the diff for a touched parity group."""

    kind: str
    name: str
    touched: tuple[str, ...]
    missing: tuple[str, ...]


# --- Anchor patterns -----------------------------------------------------

# Each pattern maps a file path to (kind, name) when applicable.
# Compiled once, matched many times.


_SHARED_AGENT_PATTERNS: tuple[tuple[re.Pattern[str], str], ...] = (
    (re.compile(r"^templates/agents/(?P<name>[^/]+)\.shared\.md$"), "template"),
    (re.compile(r"^\.claude/agents/(?P<name>[^/]+)\.md$"), "claude-install"),
    (re.compile(r"^\.github/agents/(?P<name>[^/]+)\.agent\.md$"), "github-install"),
    (re.compile(r"^src/claude/(?P<name>[^/]+)\.md$"), "src-claude"),
    (re.compile(r"^src/copilot-cli/agents/(?P<name>[^/]+)\.agent\.md$"), "src-copilot"),
    (re.compile(r"^src/vs-code-agents/(?P<name>[^/]+)\.agent\.md$"), "src-vscode"),
)

_RULE_PATTERNS: tuple[tuple[re.Pattern[str], str], ...] = (
    (re.compile(r"^\.claude/rules/(?P<name>[^/]+)\.md$"), "claude-rule"),
    (
        re.compile(r"^\.github/instructions/(?P<name>[^/]+)\.instructions\.md$"),
        "github-instruction",
    ),
    (
        re.compile(r"^src/copilot-cli/instructions/(?P<name>[^/]+)\.instructions\.md$"),
        "src-copilot-instruction",
    ),
)

# Names that exist on the src-claude side under templates/ but are intentionally
# not Claude-Code self-host candidates (e.g. ``claude-instructions.template.md``
# is a documentation fixture, not an agent prompt). Keep this list short and
# explicit. Adding entries here loosens the rule; do so only with a comment.
_NON_AGENT_SRC_CLAUDE: frozenset[str] = frozenset({"claude-instructions.template"})


def _shared_agent_members(name: str) -> tuple[str, ...]:
    return (
        f"templates/agents/{name}.shared.md",
        f".claude/agents/{name}.md",
        f".github/agents/{name}.agent.md",
        f"src/claude/{name}.md",
        f"src/copilot-cli/agents/{name}.agent.md",
        f"src/vs-code-agents/{name}.agent.md",
    )


def _rule_members(name: str) -> tuple[str, ...]:
    return (
        f".claude/rules/{name}.md",
        f".github/instructions/{name}.instructions.md",
        f"src/copilot-cli/instructions/{name}.instructions.md",
    )


# Hand-maintained install copies. When ONLY these paths move in a diff
# (no template anchor, no vendored ``src/`` copy), the change is a
# catch-up resync that brings the install side back in line with an
# already-current canonical. Block-on-asymmetry would prevent the very
# fix the validator's existence motivates; allow the resync.
_SHARED_AGENT_INSTALL_PREFIXES: tuple[str, ...] = (
    ".claude/agents/",
    ".github/agents/",
)


def _shared_agent_is_install(member: str) -> bool:
    return any(member.startswith(p) for p in _SHARED_AGENT_INSTALL_PREFIXES)


# --- Path normalization -------------------------------------------------


def _normalize_path(path: str) -> str:
    """Normalize a diff path for pattern matching.

    Converts back-slashes to forward-slashes and strips a leading ``./``
    prefix (and any repeats of it). Critically, this does NOT strip
    leading dot characters in path components such as ``.claude/`` or
    ``.github/``; an earlier draft used ``str.lstrip("./")`` and silently
    mangled every dotted directory name in the diff.
    """
    s = path.replace("\\", "/")
    while s.startswith("./"):
        s = s[2:]
    return s


# --- Anchor detection ----------------------------------------------------


def classify(path: str) -> tuple[str, str] | None:
    """Return ``(kind, name)`` for a path that anchors a parity group.

    Returns ``None`` when the path is not part of any tracked group. The
    caller decides what to do with unknown paths.

    The classification is purely syntactic; on-disk existence is not
    consulted here. ``group_for_anchor`` is the function that resolves a
    classified path into a concrete :class:`ParityGroup`.
    """
    norm = _normalize_path(path)
    if Path(norm).name in _AGENT_FILENAME_BLOCKLIST and "/agents/" in norm:
        return None
    if Path(norm).name in _RULE_FILENAME_BLOCKLIST and "/rules/" in norm:
        return None

    for pat, _label in _SHARED_AGENT_PATTERNS:
        m = pat.match(norm)
        if m:
            name = m.group("name")
            if name in _AGENT_FILENAME_BLOCKLIST:
                return None
            if name in _NON_AGENT_SRC_CLAUDE:
                return None
            return ("SHARED_AGENT", name)

    for pat, _label in _RULE_PATTERNS:
        m = pat.match(norm)
        if m:
            name = m.group("name")
            if name in _RULE_FILENAME_BLOCKLIST:
                return None
            return ("RULE", name)

    return None


def group_for_anchor(kind: str, name: str) -> ParityGroup:
    """Return the full parity group for a classified anchor."""
    if kind == "SHARED_AGENT":
        return ParityGroup(kind=kind, name=name, members=_shared_agent_members(name))
    if kind == "RULE":
        return ParityGroup(kind=kind, name=name, members=_rule_members(name))
    msg = f"unknown parity kind: {kind!r}"
    raise ValueError(msg)


# --- Group-existence shaping --------------------------------------------


def _is_shared_agent_group(
    repo_root: Path,
    name: str,
    touched_set: frozenset[str] | None = None,
) -> bool:
    """Heuristic: a name is a SHARED_AGENT group iff the template anchor is
    present on disk OR the template path is in the touched set.

    Used when a hand-maintained install path (e.g. ``.github/agents/code-reviewer.agent.md``)
    is touched. If the template anchor is absent on disk, treat the file as a
    freestanding one-member group rather than enforcing missing siblings,
    UNLESS the diff itself touches the template (delete or rename), in which
    case the parity contract still binds: a template deletion must move its
    install siblings together.
    """
    anchor_path = f"templates/agents/{name}.shared.md"
    if (repo_root / "templates" / "agents" / f"{name}.shared.md").is_file():
        return True
    if touched_set is not None and anchor_path in touched_set:
        return True
    return False


def _expected_members(
    repo_root: Path,
    group: ParityGroup,
    touched_set: frozenset[str] | None = None,
) -> tuple[str, ...]:
    """Filter a parity group's members to the ones we expect on this PR.

    For SHARED_AGENT, all six members are required when the template
    anchor exists (or the diff touches the template path, e.g. a deletion).
    When neither is true, only the freestanding install file is expected;
    the rest are skipped.

    For RULE, every member is expected unconditionally; the generator
    produces both install paths from the canonical rule.
    """
    if group.kind == "SHARED_AGENT":
        if _is_shared_agent_group(repo_root, group.name, touched_set):
            return group.members
        # Freestanding: only the github install path is required.
        github_path = f".github/agents/{group.name}.agent.md"
        return (github_path,) if github_path in group.members else group.members
    return group.members


# --- Core check ----------------------------------------------------------


def find_violations(
    touched: Iterable[str],
    repo_root: Path | None = None,
) -> list[Violation]:
    """Return parity violations across the touched-file set.

    A violation is one parity group where at least one member was touched
    and at least one expected sibling was not.
    """
    root = repo_root or _REPO_ROOT
    touched_set: set[str] = {_normalize_path(p) for p in touched}

    # Bucket touched files by classified group.
    by_group: dict[tuple[str, str], set[str]] = {}
    for path in touched_set:
        cls = classify(path)
        if cls is None:
            continue
        by_group.setdefault(cls, set()).add(path)

    touched_frozen = frozenset(touched_set)
    violations: list[Violation] = []
    for (kind, name), members_touched in sorted(by_group.items()):
        group = group_for_anchor(kind, name)

        # Special case: freestanding agents (no template anchor and the
        # template path is not in the touched set).
        # When no shared template exists, .claude/agents/ and .github/agents/
        # paths are independent one-member groups. Solo edits to either are
        # valid and should not require siblings. Without this check, editing
        # a Claude-only agent like context-retrieval falsely requires
        # .github/agents/context-retrieval.agent.md which does not exist.
        # However, when the diff itself touches the template (delete or
        # rename), the parity contract still binds.
        if kind == "SHARED_AGENT" and not _is_shared_agent_group(
            root, name, touched_frozen
        ):
            continue

        expected = _expected_members(root, group, touched_frozen)
        missing = tuple(sorted(m for m in expected if m not in touched_set))
        if not missing:
            continue
        if set(members_touched).issubset(expected) and len(expected) == 1:
            # One-member group: nothing to compare; skip.
            continue
        # Asymmetric rule: when the diff touches ONLY install members of a
        # SHARED_AGENT group (.claude/agents/ and .github/agents/), treat
        # the diff as a catch-up resync and allow it. The install copies
        # are being brought back in line with an already-current canonical
        # and the three vendored ``src/*`` copies. Without this carve-out
        # the validator blocks the very fix it exists to motivate. RULE
        # groups have no install-only role and are always strict.
        if kind == "SHARED_AGENT" and all(
            _shared_agent_is_install(m) for m in members_touched
        ):
            continue
        violations.append(
            Violation(
                kind=kind,
                name=name,
                touched=tuple(sorted(members_touched)),
                missing=missing,
            )
        )

    return violations


# --- Diff source ---------------------------------------------------------


def _git_diff_files(base: str, repo_root: Path) -> tuple[list[str], int, str]:
    """Return changed paths from ``git diff --name-only base..HEAD``.

    Returns ``(files, exit_code, error_message)``. Exit codes: 0 on
    success, 2 when git is unavailable or the base ref cannot be resolved.
    The error_message is the captured stderr (or exception text) so the
    caller can decide whether to print it (e.g. only when all bases fail).
    Callers that fall through to a backup base should suppress the
    intermediate error to avoid noisy stderr output.
    """
    try:
        proc = subprocess.run(
            ["git", "-C", str(repo_root), "diff", "--name-only", f"{base}..HEAD"],
            capture_output=True,
            text=True,
            check=False,
            timeout=30,
        )
    except (OSError, subprocess.SubprocessError) as exc:
        return [], 2, f"git diff failed: {exc}"
    if proc.returncode != 0:
        msg = (
            f"git diff --name-only {base}..HEAD exit {proc.returncode}: "
            f"{proc.stderr.strip()}"
        )
        return [], 2, msg
    return [ln for ln in proc.stdout.splitlines() if ln.strip()], 0, ""


# --- Output --------------------------------------------------------------


def _format_text(violations: Sequence[Violation]) -> str:
    if not violations:
        return "install-parity: OK"
    lines: list[str] = ["install-parity: DRIFT"]
    for v in violations:
        lines.append("")
        lines.append(f"  [{v.kind}] {v.name}")
        lines.append("    touched:")
        for p in v.touched:
            lines.append(f"      {p}")
        lines.append("    missing (required siblings):")
        for p in v.missing:
            lines.append(f"      {p}")
    lines.append("")
    lines.append(
        "Fix: include the missing files in this PR. For SHARED_AGENT, the"
        " template, the .claude/ and .github/ install copies, and all three"
        " src/* vendored copies move together. For RULE, the canonical and"
        " both install mirrors move together."
    )
    return "\n".join(lines)


def _format_json(violations: Sequence[Violation]) -> str:
    payload = {
        "drift": bool(violations),
        "violations": [
            {
                "kind": v.kind,
                "name": v.name,
                "touched": list(v.touched),
                "missing": list(v.missing),
            }
            for v in violations
        ],
    }
    return json.dumps(payload, indent=2, sort_keys=True)


# --- CLI -----------------------------------------------------------------


def _build_parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(
        description="Detect install-copy drift across parity groups.",
    )
    p.add_argument(
        "--base",
        default="@{push}",
        help=(
            "Git base ref to diff against (default: '@{push}', the configured"
            " upstream). Falls back to 'origin/main' if @{push} is unset."
        ),
    )
    p.add_argument(
        "--files",
        nargs="*",
        default=None,
        help="Explicit file list (bypass git diff). Useful for tests.",
    )
    p.add_argument(
        "--repo-root",
        type=Path,
        default=None,
        help="Override repo root (default: derived from script path).",
    )
    p.add_argument(
        "--format",
        choices=("text", "json"),
        default="text",
    )
    return p


def main(argv: Sequence[str] | None = None) -> int:
    args = _build_parser().parse_args(argv)
    repo_root = (args.repo_root or _REPO_ROOT).resolve()
    if not repo_root.is_dir():
        print(f"error: repo root not found: {repo_root}", file=sys.stderr)
        return 2

    if args.files is not None:
        touched = list(args.files)
    else:
        # Only fall back to origin/main when the caller did NOT supply an
        # explicit --base (i.e. they accepted the default @{push}).
        # Falling back from a caller-supplied base would silently validate
        # the wrong diff and turn a config error into a false clean result.
        parser_default = _build_parser().get_default("base")
        if args.base == parser_default:
            bases = [args.base, "origin/main"]
        else:
            bases = [args.base]

        touched: list[str] = []
        errors: list[str] = []
        success = False
        for base in bases:
            files, rc, err = _git_diff_files(base, repo_root)
            if rc == 0:
                touched = files
                success = True
                break
            errors.append(err)

        if not success:
            # All attempted bases failed: surface every captured error so
            # operators can see the full chain. A fallback that succeeds
            # should not pollute stderr with the primary's error.
            for err in errors:
                print(f"error: {err}", file=sys.stderr)
            return 2

    violations = find_violations(touched, repo_root=repo_root)
    output = _format_json(violations) if args.format == "json" else _format_text(violations)
    print(output)
    return 1 if violations else 0


if __name__ == "__main__":
    sys.exit(main())
