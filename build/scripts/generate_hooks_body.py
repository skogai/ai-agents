#!/usr/bin/env python3
"""Script-body manipulation for Copilot CLI hook shim injection.

Extracted from ``generate_hooks.py`` (issue #2223) so the generator stays
under the file-size taste limit. This module owns the transforms that wrap
an original Claude hook script body inside the matcher shim and reverse the
process for idempotent regeneration:

- :func:`inject_shim` / :func:`strip_shim`: the public round-trip pair.
- ``__main__`` epilogue detection and stripping (so the wrapped body does
  not run ``main()`` twice).
- ``from __future__`` import hoisting (PEP 236 requires module-level
  placement; the wrapper would otherwise indent them into a function body).

The shim source itself is generated by :mod:`generate_hooks_shim`; this
module depends on its sentinels and :func:`generate_hooks_shim._build_shim`.
"""

from __future__ import annotations

import re as _re

from generate_hooks_shim import _SHIM_BEGIN, _SHIM_END, _build_shim

_MAIN_EPILOGUE_RE = _re.compile(
    r"^if\s+__name__\s*==\s*['\"]__main__['\"]\s*:\s*\n"
    r"\s+sys\.exit\(\s*main\(\)\s*\)\s*$",
    _re.MULTILINE,
)

# Matches the try/except pattern: if __name__ == "__main__": try: main() ...
# Used by fail-open hooks that wrap main() in exception handling.
# Captures the entire block including exception handling to the end of file.
_MAIN_EPILOGUE_TRY_RE = _re.compile(
    r"^if\s+__name__\s*==\s*['\"]__main__['\"]\s*:\s*\n"
    r"\s+try:\s*\n"
    r"\s+main\(\)",
    _re.MULTILINE,
)

# Matches fail-open pattern: try/except in __main__ that exits 0 on exception.
# Used to preserve fail-open behavior when stripping the main epilogue.
_FAIL_OPEN_HANDLER_RE = _re.compile(
    r"^if\s+__name__\s*==\s*['\"]__main__['\"]\s*:\s*\n"
    r"\s+try:.*?"
    r"except.*?sys\.exit\(0\)",
    _re.MULTILINE | _re.DOTALL,
)

# Full epilogue pattern for stripping: matches from `if __name__` to end of file.
# Used by _strip_main_epilogue to remove the entire block before wrapping.
_MAIN_EPILOGUE_FULL_RE = _re.compile(
    r"^if\s+__name__\s*==\s*['\"]__main__['\"]\s*:.*",
    _re.MULTILINE | _re.DOTALL,
)


def _has_main_function_and_epilogue(body: str) -> bool:
    """Detect scripts that define ``main()`` and invoke it from a ``__main__`` guard.

    When the original script declares ``def main()`` and dispatches it via
    a ``if __name__ == '__main__':`` block (either the canonical
    ``sys.exit(main())`` or a try/except wrapper), the generated wrapper
    must invoke ``main()`` to surface its exit code. Without this, the
    wrapper falls through to the trailing ``return 0`` and every guard
    always reports success regardless of validator outcome.

    Recognized patterns:
    - ``if __name__ == '__main__': sys.exit(main())``
    - ``if __name__ == '__main__': try: main() except: ...``
    """
    has_main = _re.search(r"^def\s+main\s*\(", body, _re.MULTILINE) is not None
    if not has_main:
        return False
    return (
        _MAIN_EPILOGUE_RE.search(body) is not None
        or _MAIN_EPILOGUE_TRY_RE.search(body) is not None
    )


def _has_fail_open_handler(body: str) -> bool:
    """Detect scripts with fail-open exception handlers in their ``__main__`` block.

    A fail-open handler catches exceptions from ``main()`` and exits 0 so the
    hook allows the operation to proceed on unexpected errors. When the shim
    strips the ``__main__`` block, this behavior must be preserved in the
    generated ``_original_main`` wrapper.

    Pattern: ``if __name__ == "__main__": try: main() except: ... sys.exit(0)``
    """
    return _FAIL_OPEN_HANDLER_RE.search(body) is not None


def _strip_main_epilogue(body: str) -> str:
    """Remove the ``if __name__ == '__main__'`` block from the script body.

    When the shim wraps a script that has a main epilogue, the original block
    must be stripped to prevent double execution. The shim already invokes
    ``main()`` via the synthetic ``return main()`` trailer; leaving the
    original epilogue causes ``main()`` to run twice when ``__name__`` is
    ``'__main__'`` (which it always is when Copilot executes the shim).

    Returns the body with the epilogue removed, preserving all content before it.
    """
    match = _MAIN_EPILOGUE_FULL_RE.search(body)
    if match:
        return body[:match.start()].rstrip() + "\n"
    return body


def _wrap_body_in_function(body: str) -> str:
    """Indent ``body`` and wrap it in ``def _original_main(stdin_bytes):``.

    The original script's top-level statements become the function body.
    Scripts that follow the canonical ``def main(): ...; if __name__ ==
    '__main__': sys.exit(main())`` shape get a trailing ``return main()``
    so the wrapper surfaces the validator's real exit code, and the
    original ``if __name__ == '__main__': sys.exit(main())`` epilogue is
    stripped so the embedded block does not raise SystemExit before the
    shim's wrapper return. Other scripts keep the original ``return 0``
    fallthrough so falling off the bottom is treated as a clean exit.
    Scripts that call ``sys.exit(...)`` explicitly are unaffected:
    ``SystemExit`` propagates through the function call.

    The wrapper preserves the original script's line numbers for
    debugging by emitting a leading ``# original script begins`` marker.

    IMPORTANT: When the script has a main epilogue, we strip it before
    wrapping to prevent double execution. The synthetic ``return main()``
    is the ONLY path that invokes main; leaving the original ``if __name__``
    block would cause main() to run twice when Copilot executes the shim.

    FAIL-OPEN PRESERVATION: When the original script had a fail-open
    handler (try/except with sys.exit(0)), the wrapper preserves that
    behavior by wrapping the ``return main()`` in a try/except that
    catches exceptions and returns 0. This ensures hooks like
    ``invoke_false_completion_gate`` maintain their fail-open contract.
    """
    has_epilogue = _has_main_function_and_epilogue(body)
    has_fail_open = _has_fail_open_handler(body) if has_epilogue else False
    if has_epilogue:
        body = _strip_main_epilogue(body)
    indented = "\n".join("    " + line if line else "" for line in body.splitlines())
    if has_epilogue:
        if has_fail_open:
            trailer = (
                "    try:\n"
                "        return main()\n"
                "    except Exception as _exc:\n"
                "        sys.stderr.write('[WARNING] hook error "
                "(fail-open): ' + str(_exc) + '\\n')\n"
                "        return 0\n"
            )
        else:
            trailer = "    return main()\n"
    else:
        trailer = "    return 0\n"
    return (
        "def _original_main(stdin_bytes):\n"
        + "    # original script body begins below\n"
        + indented
        + "\n"
        + trailer
    )


_FUTURE_IMPORT_RE = _re.compile(r"^\s*from\s+__future__\s+import\b.*$")


def _split_future_imports(body: str) -> tuple[str, str]:
    """Hoist ``from __future__`` imports out of ``body`` for module-level emission.

    Python requires ``from __future__`` imports at module top level (PEP 236);
    placing them inside a function body is a SyntaxError. The matcher shim
    wraps the original script body in ``_original_main()``, so any
    ``from __future__ import ...`` that appears in the source must be lifted
    above the wrapper.

    Returns ``(future_block, rest)``. ``future_block`` is a normalized
    sequence of leading-whitespace-stripped future imports each terminated
    by ``\\n``; empty when the body has none. ``rest`` is the body with
    those lines removed.

    Round-trip safety: :func:`strip_shim` collects future imports from the
    pre-shim head and re-prepends them so re-injection is byte-stable.
    """
    future_lines: list[str] = []
    rest_lines: list[str] = []
    for line in body.splitlines(keepends=True):
        if _FUTURE_IMPORT_RE.match(line):
            stripped = line.lstrip()
            if not stripped.endswith("\n"):
                stripped += "\n"
            future_lines.append(stripped)
        else:
            rest_lines.append(line)
    return "".join(future_lines), "".join(rest_lines)


def is_shimmed(source: str) -> bool:
    """Return True when ``source`` already carries the matcher shim.

    Detection is the literal :data:`_SHIM_BEGIN` sentinel. Subsequent
    generator runs use this to drive idempotent replacement.
    """
    return _SHIM_BEGIN in source


def inject_shim(original: str, matcher: str) -> str:
    """Return original script source with the matcher shim prepended.

    Idempotent (M5-T3): if the original already carries the shim
    sentinels, the existing shim block is stripped and replaced rather
    than stacked. A second :func:`inject_shim` call yields the same
    output as a first call against the never-shimmed body, preserving
    the invariant that the file contains exactly ONE shim block.

    ``from __future__`` imports are hoisted above the shim block so the
    generated file remains a valid Python module (PEP 236 requires future
    imports at module top level; the wrapper would otherwise indent them
    into a function body and produce a SyntaxError).
    """
    stripped = strip_shim(original) if is_shimmed(original) else original
    future_block, body = _split_future_imports(stripped)
    shim: str = _build_shim(matcher)
    wrapped = _wrap_body_in_function(body)
    prefix = future_block + "\n" if future_block else ""
    return prefix + shim + "\n" + wrapped + "\n_shim_dispatch()\n"


def _find_shim_bounds(lines: list[str]) -> tuple[int, int] | None:
    """Locate the ``(begin, end)`` line indices of a shim block.

    Returns ``None`` when either sentinel is missing. Indices are inclusive
    on both ends so the caller can slice ``lines[end+1:]`` to skip past
    the shim header.
    """
    begin_idx: int | None = None
    for i, line in enumerate(lines):
        if line.rstrip("\n") == _SHIM_BEGIN:
            begin_idx = i
            break
    if begin_idx is None:
        return None
    for i in range(begin_idx + 1, len(lines)):
        if lines[i].rstrip("\n") == _SHIM_END:
            return begin_idx, i
    return None


def _extract_original_body(after_shim: list[str]) -> str:
    """Reconstruct the original script body from post-shim lines.

    Expects the layout :func:`inject_shim` writes:
    optional blank lines, ``def _original_main(stdin_bytes):``, an
    optional ``# original script body begins below`` marker, the
    indented body, a synthetic trailing ``return 0`` or ``return main()``,
    and the final ``_shim_dispatch()`` invocation. Anything past the
    dispatch call is returned verbatim as the tail.

    When the trailer is ``return main()``, the original script had a
    ``if __name__ == '__main__'`` epilogue that was stripped during
    injection. We restore a canonical epilogue so the round-trip is
    stable (re-injection sees the same pattern and re-strips it).

    When no wrapper is detected (shim block was present but the
    function wrapper is not), the lines are returned as-is so callers
    fall back to a strip-only behavior.
    """
    j = 0
    while j < len(after_shim) and not after_shim[j].strip():
        j += 1
    if j >= len(after_shim) or not after_shim[j].startswith("def _original_main("):
        # No wrapper: caller will fall back to plain strip.
        return "".join(after_shim[j:])

    # Drop the def line and any leading body marker.
    j += 1
    if j < len(after_shim) and after_shim[j].lstrip().startswith(
        "# original script body begins below"
    ):
        j += 1

    body_lines: list[str] = []
    while j < len(after_shim):
        line = after_shim[j]
        if not line.strip():
            body_lines.append("\n")
            j += 1
            continue
        if line.startswith("    "):
            body_lines.append(line[4:])
            j += 1
            continue
        break  # Non-indented line ends the function body.

    # Trim the synthetic trailing ``return 0``, ``return main()``, or the
    # fail-open try/except block we appended. The exact trailer depends on
    # whether the original script had a canonical main() epilogue and
    # whether it had a fail-open handler at generation time.
    while body_lines and body_lines[-1].strip() == "":
        body_lines.pop()
    had_main_epilogue = False
    had_fail_open = False

    # Check for fail-open trailer pattern (multi-line try/except block).
    # The trailer structure after de-indentation is:
    #   body_lines[-5]: "try:"
    #   body_lines[-4]: "    return main()"
    #   body_lines[-3]: "except Exception as _exc:"
    #   body_lines[-2]: "    sys.stderr.write(...)"
    #   body_lines[-1]: "    return 0"
    if (
        len(body_lines) >= 5
        and body_lines[-1].strip() == "return 0"
        and "stderr.write" in body_lines[-2]
        and "except" in body_lines[-3]
        and body_lines[-4].strip() == "return main()"
        and body_lines[-5].strip() == "try:"
    ):
        had_main_epilogue = True
        had_fail_open = True
        # Remove the 5-line try/except block
        for _ in range(5):
            body_lines.pop()
        while body_lines and body_lines[-1].strip() == "":
            body_lines.pop()
    elif body_lines and body_lines[-1].strip() == "return main()":
        had_main_epilogue = True
        body_lines.pop()
        while body_lines and body_lines[-1].strip() == "":
            body_lines.pop()
    elif body_lines and body_lines[-1].strip() == "return 0":
        body_lines.pop()
        while body_lines and body_lines[-1].strip() == "":
            body_lines.pop()

    # Skip a single trailing ``_shim_dispatch()`` invocation if present.
    while j < len(after_shim) and not after_shim[j].strip():
        j += 1
    if j < len(after_shim) and after_shim[j].strip() == "_shim_dispatch()":
        j += 1

    body_text = "".join(body_lines)
    tail = "".join(after_shim[j:])
    if body_text and not body_text.endswith("\n"):
        body_text += "\n"

    # Restore a canonical epilogue when the trailer was ``return main()``
    # (with or without fail-open). This ensures re-injection produces the
    # same output (idempotent).
    if had_main_epilogue:
        if had_fail_open:
            body_text += (
                '\nif __name__ == "__main__":\n'
                "    try:\n"
                "        main()\n"
                "    except Exception:\n"
                "        sys.exit(0)\n"
            )
        else:
            body_text += '\nif __name__ == "__main__":\n    sys.exit(main())\n'

    return body_text + tail


def strip_shim(source: str) -> str:
    """Remove a previously-injected shim block. M5-T3 idempotency.

    Detection is by exact sentinel string match (see :func:`_find_shim_bounds`).
    The shim header, the ``def _original_main`` wrapper, and the trailing
    ``_shim_dispatch()`` call are all removed; the original script body
    is restored from the wrapper's indented lines.

    Future-import hoist round-trip: :func:`inject_shim` lifts
    ``from __future__`` imports above the shim. On strip, those lines
    appear in the pre-shim head; we re-prepend them to the reconstructed
    body so a strip-then-inject cycle is byte-stable.
    """
    if _SHIM_BEGIN not in source:
        return source
    lines = source.splitlines(keepends=True)
    bounds = _find_shim_bounds(lines)
    if bounds is None:
        return source
    begin_idx, end_idx = bounds
    head = "".join(lines[:begin_idx])
    body = _extract_original_body(lines[end_idx + 1 :])
    # The pre-shim head only ever holds hoisted ``from __future__``
    # imports plus an injected blank-line separator. Re-prepend the
    # imports to the body and drop any other whitespace so a strip-then-
    # inject cycle is byte-stable. Any non-future content in the head is
    # an external edit; the file is "DO NOT EDIT BY HAND" so we discard
    # rather than try to round-trip unspecified content.
    head_future, _ = _split_future_imports(head)
    return head_future + body
